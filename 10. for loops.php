<h1> for loop in PHP </h1>

<?php
    for ($i=0; $i <= 10; $i++) { 
        echo "$i <br>";
    }
?>

// * Running process : 

// ^ 1. Initialize $i = 0

// ^ 2. check condition whether it is true or not $i <= 10

// ^ 3. If condition is true , then execute the code present inside curly braces {}

// ^ 4. Updation $i++ ; After executing code , update the code .